#operasi aritmatika

#operasi penambahan +
a = 10
b = 3
hasil = a + b
print(a,'+',b,'=',hasil)

#operasi pengurangan -
a = 10
b = 3
hasil = a - b
print(a,'-',b,'=',hasil)

#operasi perkalian *
a = 10
b = 3
hasil = a * b
print(a,'*',b,'=',hasil)

#operasi pembagian /
a = 10
b = 3
hasil = a / b
print(a,'/',b,'=',hasil)

#operasi eksponen (pangkat) **
a = 10
b = 3
hasil = a ** b
print(a,'**',b,'=',hasil)

#operasi modulus % (sisa bagi)
a = 10
b = 3
hasil = a % b
print(a,'%',b,'=',hasil)

#operasi floor division // (pembagian tapi dibulatkan kebawah)
a = 10 
b = 3
hasil = a // b
print(a,'//',b,'=',hasil)

