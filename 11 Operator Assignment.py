print ("nilai a -= 2, nilai a menjadi", a)

a *= 5 #artinya adalah a = a * 5
print ("nilai a *= 5, nilai a menjadi", a)

a /= 2 #artinya adalah a = a / 2
print ("nilai a /= 2, nilai a menjadi", a)

